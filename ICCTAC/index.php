		<div class="well well-sm"><b>Kristu Jayanti College, Bangalore</b></div>
		<p align="justify">Kristu Jayanti College, founded in 1999, is run by "BODHI NIKETAN TRUST", formed by the members of St. Joseph Province of the Carmelites of Mary Immaculate (CMI). The college is affiliated to Bangalore University and is accredited with highest grade 'A' by NAAC. The college is recognized by UGC under the category 2(f) and 12 (b). The college was accorded autonomous status from 2013 by the University Grants Commission, Government of Karnataka & the Bangalore University. The Institution is rated as the one of the best colleges in India and one among the top 10 commerce colleges in Bangalore by India Today - Nielsen survey 2010 , 2011 , 2012 , 2013, 2014 & 2015. The institution strives to fulfill its mission to provide educational opportunities to all aspiring youth to excel in life by developing academic excellence, fostering values, creating civic responsibility and building global competencies in a dynamic environment.

College offers 15 UG courses, 8 PG courses, 2 Ph.D Programmes and 3 PG Diploma Programmes. The vision of the management, commitment of the faculty, co-operation of the parents, students and society at large are the main impetus for the success of the college.</p>
<div class="well well-sm"><b>Department of Computer Science (PG)</b></div>
		<p align="justify">College offers Masters Degree in Computer Applications (MCA) with the intention to produce competitive computer professionals in tune with advanced technical concepts. Master of Computer Applications (MCA), started in 2004, is affiliated to the Bangalore University and approved by AICTE. The Department strives to impart substantial knowledge in problem solving techniques, competencies for employability and integrity by inculcating ethical values.The desire to pursue continued professional development by seeking out knowledge and experience is achieved by conducting sessions beyond the classrooms.  </p>
		
