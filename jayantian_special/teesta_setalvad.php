									<p align=justify> Padmashri Teesta Setalvad while addressing the students at Vichaarmanthan said, "that our children should be taught about inequality and communalism to understand the realities existing in our country", at Kristu Jayanti College. Vichaarmanthan is a platform for students to interact with global and Indian visionaries, and churn their thoughts, to implement the best into reality. Teesta Setalvad, Popular Civil Activist & Journalist, was the chief guest of the Seventeenth edition of Vichaarmanthan. Rev. Fr. Sebastian T. A, Principal, Kristu Jayanti College; Rev. Fr. Jose P.J, Financial Administrator & Head, Department of Social Work; Rev. Fr. Augustine George, Dean, Science; Prof. Gopakumar A. V, Dean, Humanities, were present.</p>

									<p align=justify>Teesta Setalvad began by regarding the prominence of the Indian Constitution, "The founding fathers of our constitution chose to give ourselves a secular constitution, where all citizens are equal. However, this is not the reality even though they went through a slow, painful and assertive process to fight for our rights by the constitution".Taking the example of the Mahatma Gandhi's assassination, Teesta Setalvad posed to the Jayantians, "How as Indians were we so evasive and indecisive in fighting against the first act of terrorism of the nation? In this secular, democratic and egalitarian nation, has the caste system vanished? To which she replied, "We shouldn't believe in battering hatred and religion in ideas. Inequality hasn't gone away, but rather only a way around it has been found." She went on to speak with regard to the issue of Sinhala majority in Sri Lanka, "It's only with inclusiveness, contentment to share common ideas in any town, city or place, we will be able to battle any force that creates a cancerous effect in society."</p>

									<p align=justify>Brushing through the pages of Indian History, across a span of a century, "Our textbooks described mutiny of the first war of Indian independence, how we threw of British colonialism with a determined effort, and how the economic bloodline was sucked out of our democracy" Reflecting on the Indian Partition, she added, "Partition was misconceived to be one sided - towards the Muslims and prominent leaders of the Muslim league at that time. Did violence happen only against one community? In response to which she said, "We should look at history with sensibility and rationality, not peculiarity. Nation building is about shared economic, social, and cultural equality, not about religion".</p>

									<p align=justify>Teesta Setalvad encouraged the students to reduce public hatred on the internet, rather create a balance of aggression with an inquisitive understanding into the happenings at the government level. "Can India become a power without solving its own problems? The solution is that each one of us should be roused to find the courage to speak up." She went on to encourage an interactive session in which she answered questions relating to agenda setting of the present media houses, and how they are being influenced by politics. "We need to make our disapproval known to the media publicly and questioned the situation and what we stand for. The media should realize that we are not powerless as consumers for a product." To a question on the implementation of Right to Reject", she opined that constituency sizes should be smaller, "Right to Recall" or referendum should be implemented and there should be right to proportional representation of the people.</p>

									

