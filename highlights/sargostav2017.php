                                        <span>&nbsp; Sargostav - 2017  <strong>P G intra-collegiate Cultural Fest</strong></span></h4>

									
									<p align=justify>Sargotsav is the annual PG intra collegiate cultural fest organised by Kristu Jayanti College which serves as a platform where young vibrant students showcase their talents, ideas & enthusiasm. The cultural fest aims at cultivating team spirit, perfecting one’s personality & creating a civilized harmonious, positive & progressive campus culture.</p>

									<p align=justify>The Programme coordinators for Sargostav 2016-17 were Dr. Emmanuel Daniel (Faculty, Department of Social Work) and Dr. Chellaraj Emmanuel (Faculty, Department of Life Science) who functioned under the guidance of Dr. Jonas Richard A. (Head, Department of Social Work) and Dr. Elsie C.D. (Head, Department of Life Science) respectively. The Student Programme Coordinators were Mr. Valentine Anthony Raj & Ms. Zita Varghese (Department of Social Work) and Ms. Juby J. Samuel & Ms. Nidhi Prakash (Department of Life Science).</p>

									<p align=justify>Sargotsav’17 comprised of 17 different events where the individual events were scheduled for the 9th and 10th February 2017 and the Group events on the 13th February. The grand finale of Sargotsav’17 was experienced on 13th February 2017 which began with an Inaugural Ceremony followed by Group events and concluded with a Valedictory session.</p>

									<p align=justify>The Dignitaries seated on the dais for the Inauguration were the Guest of Honour Mr. Ravish Dhamija (Director, LOMA India& SriLanka), Rev. Fr. Augustine George (Vice Principal, Kristu Jayanti College), Dr. Jonas Richard A. (Head, Department of Social Work), Dr. Emmanuel Daniel (Programme Coordinator & Faculty Department of Social Work) and Mr. Valentine Anthony Raj & Ms. Zita Varghese (Student Programme Coordinators, Department of Social Work).</p>

									<p align=justify>The Welcome address was delivered by Dr. Emmanuel Daniel (Programme Coordinator & Faculty, Department of Social Work). Rev. Fr. Augustine George (Vice Principal, Kristu Jayanti College) delivered the Presidential Address followed by the Inaugural Address by the Chief Guest Mr. Ravish Dhamija (Director, LOMA India& SriLanka). Mr. Dhamija from personal experience enlightened the gathering on the significance of focussing on one’s purpose of life and expressing oneself confidently. The Chief Guest narrated instances from his earlier days that motivated him to lead a disciplined life consequently maintaining fitness and acquiring Professional Excellence. Rev. Fr. Augustine George felicitated Mr. Ravish Dhamija with a bouquet of flowers and presented a memento as a token of appreciation. The Inaugural session concluded with Vote of Thanks delivered by Ms. Zita Varghese (Student Programme Coordinator, Department of Social Work).</p>

									<p>The Inaugural Ceremony was followed by the Group events where young vibrant students showcased their talents and enthusiasm.</p>

									<p align=justify>Sargotsav’17 concluded with a Valedictory session which aimed at awarding the winners of the Fest. The dignitaries seated on the dais were Rev. Fr. Josekutty P.D. (Principal, Kristu Jayanti College), Dr. Elsie Joy (Head, Department of Life Science), Dr. Chellaraj Emmanuel (Programme Coordinator & Faculty, Department of Life Science) and Ms. Juby J. Samuel & Ms. Nidhi Prakash (Student Programme Coordinators, Department of Life Science).</p>

									<p>The Welcome address was given by Ms. Juby J. Samuel (Student Programme Coordinator, Department of Life Science).</p>

									<p align=justify>Rev. Fr. Josekutty P.D. (Principal, Kristu Jayanti College) while addressing the gathering congratulated the winners and encouraged the participants to embrace their talents. Rev. Fr. Josekutty P.D. reminded the students that competition should always be healthy and students should most importantly better themselves in the process. The valedictory session concluded with Vote of Thanks given by Ms. Nidhi Prakash (Student Programme Coordinator, Department of Life Science).</p>

