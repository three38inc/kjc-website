			<p align=justify>Industry Institute Interface popularly termed as 3i is an initiative to bridge the gap between corporate and Institute in order to equip the students with technical and managerial changes and dynamics. To cope up with the increasing pace and metamorphosis in modern corporate scenario, students need to be exposed to the aura of corporate life.</p>

			<p align=justify>The department of management studies of Kristu Jayanti School of Management had organized a Corporate Interface session for 1st Trimester MBA & PGDM students on 25th July which was presided by Mr. Fabian Simmons, Director-Revenue Operations, Oracle India Pvt Ltd. During the session, the students were divulged to the core principles of life by focusing on excellence. Life is short and thus one needs to find a meaningful purpose in life- to live, to love, to learn and to unlearn things in life to integrate a balanced happier life. In order to achieve the purpose of life, the fire (zeal) must be kept burning within to march ahead in life and to achieve the ultimate desired GOAL. Onward to the journey of attaining the goal, the focus should be towards pro-activeness rather than reactiveness which generate a positive source of energy by expanding the circle of influence in life.</p>

			<p align=justify>The students were made to understand the necessity of leading a balance, integrated and a powerful life by developing their EQ (Emotional Quotient), PQ (Physical Quotient), SQ (Spiritual Quotient) and IQ (Intelligence Quotient) to excel and attain success in life.</p>


